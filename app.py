import streamlit as st
import pickle
import numpy as np

model=pickle.load(open("model.pkl","rb"))

st.set_page_config(
page_title="Customer Churn Predictor",
layout="centered"
)

st.title("Customer Churn Prediction System")

st.write(
"Predict whether customer will churn"
)

age=st.number_input(
"Age",
18,
80,
30
)

frequent=st.selectbox(
"Frequent Flyer",
["No","Yes","No Record"]
)

income=st.selectbox(
"Annual Income Class",
["Low Income",
"Middle Income",
"High Income"]
)

services=st.slider(
"Services Opted",
1,
6,
2
)

social=st.selectbox(
"Account Synced To Social Media",
["No","Yes"]
)

hotel=st.selectbox(
"Booked Hotel",
["No","Yes"]
)

ff_map={
"No":0,
"Yes":1,
"No Record":2
}

income_map={
"Low Income":0,
"Middle Income":1,
"High Income":2
}

yesno={
"No":0,
"Yes":1
}

if st.button("Predict Churn"):

 data=np.array([[
 age,
 ff_map[frequent],
 income_map[income],
 services,
 yesno[social],
 yesno[hotel]
 ]])

 pred=model.predict(data)

 if pred[0]==1:
     st.error(
     "Customer likely to churn"
     )
 else:
     st.success(
     "Customer likely to stay"
     )